list1 = ["m","a","m"]
list2=[1,2,3]

copy_list1=list1.copy()
copy_list1.reverse()

if(copy_list1 == list1):
    print("list1 is palindrome")
else:
    print("list1 is not a palindrome")

copy_list2=list2.copy()
copy_list2.reverse()

if(copy_list2 == list2):
    print("list1 is palindrome")
else:
    print("list1 is not a palindrome")
